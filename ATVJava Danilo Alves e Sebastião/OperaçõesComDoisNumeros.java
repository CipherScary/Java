package ATVJava;

public class OperaçõesComDoisNumeros {
    public static void main(String[] args) {
        int num1 = 5; // declara numero
        int num2 = 3; // declara numero
        
        int soma = num1 + num2; // declara a funçao do calculo
        int subt = num1 - num2; // declara a funçao do calculo
        int mult = num1 * num2; // declara a funçao do calculo
        int div = num1 / num2;  // declara a funçao do calculo

        System.out.println("Soma: " + soma); // imprime a soma
        System.out.println("Diferença: " + subt); // imprime a diferença
        System.out.println("Produto: " + mult); // imprime o produto
        System.out.println("Quociente: " + div); // imprime o quociente
    }
}

