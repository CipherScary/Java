package ATVJava;

public class ParOuImpar {
    public static void main(String[] args){
        int numero = 5;  // declara numero
        if (numero % 2 == 0) { // comando if
            System.out.println("É par!"); // imprime o resultado par
        } else { // comando else
            System.out.println("É impar!"); // imprime o resultado impar
        }
    }
}
