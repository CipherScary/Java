package ATVJava;

public class PositivoOuNegativo {
    public static void main(String[] args){
        int numero = 5; // declara numero
        if (numero >= 0) { // comando if
            System.out.println("numero positivo"); // imprime o resultado positivo
        } else { // comando else
            System.out.println("numero negativo"); // imprime o resultado nagativo
        }
    }
}
